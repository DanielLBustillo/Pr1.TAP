

public class Pista {
	
	protected String idPista; 
	protected boolean disponibilidadPista; 

	
	public Pista() {
		
	}
	
	
	public String getIdPista() {
		return idPista;
	}
	public void setIdPista(String idPista) {
		this.idPista = idPista;
	}
	public boolean isDisponibilidadPista() {
		return disponibilidadPista;
	}
	public void setDisponibilidadPista(boolean disponibilidadPista) {
		this.disponibilidadPista = disponibilidadPista;
	}
	
	
	
	

}
